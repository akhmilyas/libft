#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	unsigned int	tmp;

	if (n < 0)
	{
		ft_putchar_fd('-', fd);
		tmp = (unsigned int)(n * -1);
	}
	else
		tmp = (unsigned int)n;
	if (tmp >= 10)
	{
		ft_putnbr_fd(tmp / 10, fd);
		ft_putnbr_fd(tmp % 10, fd);
	}
	else
		ft_putchar_fd((tmp % 10 + '0'), fd);
}
